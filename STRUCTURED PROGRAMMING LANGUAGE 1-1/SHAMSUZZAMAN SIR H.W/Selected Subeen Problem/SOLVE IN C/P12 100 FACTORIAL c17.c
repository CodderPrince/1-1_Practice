#include<stdio.h>
int main()
{
    int t;
    scanf("%d",&t);
    while(t--)
    {
        int n,i, count=0;
        scanf("%d",&n);
        for(i=5; i<=n; i= i*5)//1-4 factorial er last e zero nei
                            // sorbonimno 1ta zero ase 5-9 porjonto
                            //jehetu proti 5 number porpor 1ta kore zero increse
                            // hosse tai i er man 5 dara multiply kora hoyese
        {
            count= count + n/i;
        }
        printf("%d\n", count);
    }
    return 0;
}
